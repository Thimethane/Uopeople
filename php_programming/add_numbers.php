<?php
// Program to add all integers between 0 and 50 using a 'for' loop

$total = 0; // Initialize total to 0

// Loop through numbers from 0 to 50
for ($i = 0; $i <= 50; $i++) {
    $total += $i; // Add the current number to the total
}

// Display the total
echo "The total of integers between 0 and 50 is: $total\n";
?>
